  /// الحصول على قائمة بأسماء المناطق
  static List<String> getRegions() {
    return regionsWithCities.keys.toList();
  }

  /// الحصول على المدن حسب المنطقة
  static List<String> getCitiesByRegion(String region) {
    return regionsWithCities[region] ?? [];
  }

  /// البحث عن مدن
  static List<String> searchCities(String query) {
    if (query.isEmpty) return [];
    
    final results = <String>[];
    regionsWithCities.forEach((region, cities) {
      results.addAll(
        cities.where((city) => city.contains(query)),
      );
    });
    return results;
  }
